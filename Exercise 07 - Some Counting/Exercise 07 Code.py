print("Loop 1 : ")                                                           # a loop that counts up from 0 to 50 in increments of 1
for i in range(51):
    print(i)

print("Loop 2 : ")                                                           # a loop that counts down from 50 to 0 in decrements of 1
for i in range(50,-1,-1):
    print(i)

print("Loop 3 : ")                                                           # a loop that counts up from 30 to 50 in increments of 1
for i in range (30,51):
    print(i)

print("Loop 4 : ")                                                           # a loop that counts down from 50 to 10 in decrements of 2
for i in range (50,8,-2):
    print(i)

print("Loop 5 : ")                                                           # a loop that counts up from 100 to 200 in increments of 5
for i in range(100,205,5):
    print(i)